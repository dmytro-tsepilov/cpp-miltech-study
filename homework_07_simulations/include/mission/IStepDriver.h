#pragma once
// IStepDriver — hook-інтерфейс, що дозволяє MissionProcessor::run() керувати
// зовнішнім циклом кроку (UART/GPIO), не знаючи деталей вводу-виводу.
//
// Місія викликає ці хуки навколо кожного step():
//   waitNextTick() — заблокуватися до готовності наступного кадру вводу
//                    (пейсинг). Повернути false, щоб завершити цикл місії
//                    (зупинка / більше немає кадрів).
//   beforeStep()   — подати свіжий ввід (телеметрію) у джерело стану дрона
//                    ПЕРЕД кроком наведення.
//   afterStep()    — віддати рішення місії (команду CONTROL) ПІСЛЯ кроку.
//   onDrop()       — одноразова дія скиду (імпульс GPIO + нульове керування).
//
// У файловому/HTTP режимі драйвер не встановлюється — run() використовує
// звичний пейсинг через TimeProvider.

#ifndef I_STEP_DRIVER_H
#define I_STEP_DRIVER_H

class IStepDriver {
public:
    virtual ~IStepDriver() = default;

    // Заблокуватися до наступного такту/кадру. false = зупинити цикл місії.
    virtual bool waitNextTick() = 0;

    // Подати свіжий стан (телеметрію) перед кроком наведення.
    virtual void beforeStep() = 0;

    // Віддати рішення керування місії після кроку наведення.
    virtual void afterStep() = 0;

    // Одноразова дія скиду (напр. імпульс GPIO + нульове керування).
    virtual void onDrop() = 0;
};

#endif // I_STEP_DRIVER_H
